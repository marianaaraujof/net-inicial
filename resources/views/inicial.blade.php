<h1>Quem somos?</h1>
<p>A Escola de Programação do Laboratório de Inovação Tecnologica em Saúde - LAIS,
da Universidade Federal do Rio Grande do Norte - UFRN, implementou ações educativas para a formação tecnológica,
com expertise, de seus discentes. 
Fazendo uma interface entre a Teoria dos Estilos de Aprendizagem, de David Kolb,
e a metodologia ativa de aprendizagem baseada em problemas (Problem Based Learning), desenvolveu
método para a formação acadêmica e profissional de alunos de áreas interdisciplinares como
Engenharia Biomédica, Engenharia Mecânica, Tecnologia da Informação e Design. A aplicação da
metodologia da aprendizagem baseada em problemas permitiu, de forma párica e contextualizada
o recebimento da demanda-problema, seguido da busca pelo conhecimento para a formulação de hipóteses 
e a definição de parâmetros objetivando possíveis resoluções, o aprendizado de diversas linguagens
de programação para o desenvolvimento de sistemas computacionais na área da saúde, gerenciamento
da educação para saúde e gerenciamento de informações. 
Todos para o atendimento à Instituições de Ensino Superior Nacionais e Internacionais e 
Hospitais Universitários. A imersão dos estudantes de diversos perfis e estilos de aprendizagem diferenciados
em um contexto de problemas reais, potencializou a experiência, tornando-a exitosa e significativa
por meio das habilidades adquiridas durante todo o processo. O desenvolvimento do 
conhecimento acontece de forma natural e por meio das ações requeridas no contexto de 
imersão dos estudantes, cpntemplando e fazendo uso das características presentes nos 
estilos de aprendizagem predominantes em cada um, permitindo a interdisciplinaridade e a 
troca de saberes distintos entre a equipe. Este processo contínuo culminou no 
desenvolvimento de sistemas computacionais para uso na área educacional, informacional,
gerencial e de sáude, a fim de otimizar tempo, recursos e informações, solucionando os 
problemas apresentados em seus locais e contextos específicos.</p>


